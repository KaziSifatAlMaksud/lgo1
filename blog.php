                 An energy audit is a process of evaluating the energy usage of a building, Plant or facility and identifying areas where energy efficiency improvements can be made. The objective of an energy audit is to identify areas of energy waste and recommend energy-efficient solutions to reduce energy consumption and costs.
An energy audit can be a valuable tool the organizations who looking to reduce their energy costs, improve their sustainability, and reduce their carbon footprint. In some cases, energy audits may also be required by local or state regulations, particularly for large commercial and industrial facilities.
Energy audit service is conducted by Lgorithm Services Limited by Certified and experienced professional energy auditor who are trained to conduct a comprehensive energy assessment of a facility or building.
In the time of an energy audit, the auditor of Lgorithm Services Limited collect data on the energy usage patterns of the building and analyze the data to identify areas where energy is being wasted. This may include evaluating the efficiency of heating and cooling systems, lighting systems, and other energy-consuming equipment.
During The energy audit process we typically do the below:
1.	Gathering data: This includes analyzing energy bills, examining building plans, and conducting interviews with facility managers and occupants to gain an understanding of the building's energy use and patterns.
2.	On-site inspection: Our auditor conducted a thorough inspection of the building, including its heating, ventilation, and air conditioning (HVAC) system, lighting, and other energy-consuming systems.
3.	Analysis: Lgorithm auditor analyze the data collected during the inspection and identify areas where energy is being wasted.
4.	Recommendations: Based on the analysis, the auditor will recommend energy-efficient solutions to reduce energy consumption and costs. This may include upgrades to lighting, HVAC systems, insulation, and other energy-consuming systems.
5.	Reporting: The auditor will provide a detailed report that outlines the findings, recommendations, and potential savings from implementing the recommended energy-efficient solutions.
